package com.oj.jxc.entity;

import java.io.Serializable;

/**
 * 数据对象
 * @since 2014-02-26
 */
public class SRegionDO implements Serializable {

    private static final long serialVersionUID = 139338904629092937L;

    /**
     * column s_region.id  自增关联编号
     */
    private Integer id;

    /**
     * column s_region.province  省
     */
    private String province;

    /**
     * column s_region.city  市
     */
    private String city;

    /**
     * column s_region.county  县
     */
    private String county;

    public SRegionDO() {
        super();
    }

    public SRegionDO(Integer id, String province, String city, String county) {
        this.id = id;
        this.province = province;
        this.city = city;
        this.county = county;
    }

    /**
     * getter for Column s_region.id
     */
    public Integer getId() {
        return id;
    }

    /**
     * setter for Column s_region.id
     * @param id
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * getter for Column s_region.province
     */
    public String getProvince() {
        return province;
    }

    /**
     * setter for Column s_region.province
     * @param province
     */
    public void setProvince(String province) {
        this.province = province;
    }

    /**
     * getter for Column s_region.city
     */
    public String getCity() {
        return city;
    }

    /**
     * setter for Column s_region.city
     * @param city
     */
    public void setCity(String city) {
        this.city = city;
    }

    /**
     * getter for Column s_region.county
     */
    public String getCounty() {
        return county;
    }

    /**
     * setter for Column s_region.county
     * @param county
     */
    public void setCounty(String county) {
        this.county = county;
    }

}